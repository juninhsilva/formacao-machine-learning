# Condicionais simples
a = 12
b = -9
soma = a + b

if(soma > 0):
    print('maior que zero')

if(soma < 2):
    print('maior que zero')
else:
    print('não é maior que zero')

# Condicionais aninhadas
if(soma < 0):
    print('menor que zero')
elif (soma == 2):
    print('igual a 2')
else:
    print('diferente de 2')